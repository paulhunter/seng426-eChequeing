    // Verify cheque information provided
    private void jMVerifyChequeActionPerformed(java.awt.event.ActionEvent evt) {
     
        String certificatePath;
        if(loadChequeFlag){
            certificatePath = getFileLocation("Load Digital Certificate");   
            
            if(certificatePath.length()!=0){
                //get the cheque reference sigantured. 
                String signatureRef= ChequeReferenceString(oldCheque);
            
                //Create Digital Certificate Object for verification
                DigitalCertificate drawerDC = new DigitalCertificate();
            
                // Create Digital Certificate IO to load the Certificate
                DigitalCertificateIO readDC = new DigitalCertificateIO();
           
                try{
                    drawerDC = (DigitalCertificate)readDC.readDigitalCertificate(certificatePath);
                    boolean verfiySign;
                    verfiySign = DigitalSignature.verifyDS(oldCheque.getDrawerSignature(),signatureRef,drawerDC.getpublicKey());
            
                    if(verfiySign){
                        JOptionPane.showMessageDialog(null, "The signature is vaild", "e-Cheque Clear", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "The signature is not vaild", "e-Cheque not Clear", JOptionPane.WARNING_MESSAGE);
                    }
                }
                catch(Exception exp){
                    JOptionPane.showMessageDialog(null, "Digital Certificate Corrupted", "Unknwon error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "You have to load the drawer Digital Certificate", "User Error", JOptionPane.ERROR_MESSAGE);
            }
        }else {
            JOptionPane.showMessageDialog(null, "You have to open cheque", "User Error", JOptionPane.ERROR_MESSAGE);
        }   
    }

