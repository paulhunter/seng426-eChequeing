    // Sets a reference for the cheque
    private String ChequeReferenceString(ECheque chq){
        
        if(chq.getAccountNumber() == ""){
            return "";
        }

        String ref = "";
        ref+= chq.getAccountNumber()+chq.getAccountHolder()+chq.getBankName()+chq.getChequeNumber()+chq.getAmountOfMoney()+chq.getCurrencyType()+chq.getEarnDay()+chq.getGuaranteed()+chq.getPayToOrderOf();
       
        return ref;       
    }  
