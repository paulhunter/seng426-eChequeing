    // Save cheque
    private void jMSaveChequeActionPerformed(java.awt.event.ActionEvent evt) {
        String amount;
        String payTo;
        String day;
        String month;
        String year;
        boolean guaranteed = false;
               
        // get cheque info to save it.
        amount = jTAmount.getText();
        payTo = jTPayTo.getText();
        day = jTDay.getText();
        month = jTMonth.getText();
        year = jTYear.getText();

        if(jCGuaranteed.isSelected()){
            guaranteed = true;
        }
         
        // Make sure all check data is present
        // Note that the if statements are broken apart
        // so that individualized errors may be given
        if(newChequeFlag){
            if(amount.length()!=0){
                if(payTo.length()!=0){
                    if(day.length()!=0 && month.length()!=0 && year.length()!=0){
                        // Ask the user to enter his password to sign the cheque with his private key
                        getSign();
                        if(signPass.hashCode()==eChequeReg.getPasword()){    
                            ECheque chequeObj = new ECheque();
                            try{
                                //create AES Key with user password and cipher  
                                Key AESKey = AESCrypt.InitializeAESKeyByPassword(signPass);
                                Cipher cipher = AESCrypt.InitializeCipher(AESKey,1);
                                InputStream in = new FileInputStream(eChequeReg.getEWalletLoaction()+"\\Security Tools\\Private Key.key");
                                OutputStream out = new FileOutputStream(eChequeReg.getEWalletLoaction()+"\\Security Tools\\PrivateKey.key"); 
                                   
                                // decrypt the private key with the AES key and delete the plain key
                                AESCrypt.Crypt(in,out,cipher);
                                in.close();
                                out.close();
                                ObjectInputStream objIn = new ObjectInputStream (new FileInputStream(eChequeReg.getEWalletLoaction()+"\\Security Tools\\PrivateKey.key"));
                                   
                                // load the user private key.
                                PrivateKey privKey = (PrivateKey)objIn.readObject();
                                objIn.close();
                                   
                                // delete the un secure key.
                                File control = new File(eChequeReg.getEWalletLoaction()+"\\Security Tools\\PrivateKey.key");
                                control.delete();
                                JOptionPane.showMessageDialog(null,"Load private key");
                                   
                                // Fill the Cheque Data
                                chequeObj.setaccountNumber(eChequeReg.getAccountNumber());
                                chequeObj.setAccountHolder(eChequeReg.getClientName());
                                chequeObj.setBankName(eChequeReg.getBankName());
                                chequeObj.setChequeNumber(jLSerialNumber.getText());
                                chequeObj.setAmountOfMoney(jTAmount.getText());
                                chequeObj.setCurrencyType("US $");
                                chequeObj.setAmountOfMoney(jTAmount.getText());
                                chequeObj.setEarnDay(year+","+month+","+day);
                                chequeObj.setPayToOrderOf(payTo);
                                chequeObj.setGuaranteed(guaranteed);
                                   
                                // get cheque reference string and sign it.
                                String chequeRef = ChequeReferenceString(chequeObj);
                                chequeObj.setDrawerSignature(DigitalSignature.signDS(chequeRef, privKey));
                                JOptionPane.showMessageDialog(null,"Sign Complete");
                                   
                                //Save the cheque after you sign it
                                EChequeIO drawCheque = new EChequeIO();
                                drawCheque.writeECheque(chequeObj,eChequeReg.getEWalletLoaction()+"\\My Cheques\\"+chequeObj.getChequeNumber()+".sec");
                                JOptionPane.showMessageDialog(null,"Done");
       
                            }catch(Exception exp){
                                JOptionPane.showMessageDialog(null, exp.getMessage(), "System error", JOptionPane.ERROR_MESSAGE);
                            }
                        }else{
                            JOptionPane.showMessageDialog(null, "Incorect passowrd", "User Error", JOptionPane.ERROR_MESSAGE);
                        }   
                    }else{
                        JOptionPane.showMessageDialog(null, "Complete the earn date info", "User Error", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "You have to specify the cheque receiver", "User Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "You can not write an empty cheque", "User Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "You should create a new cheque first", "User Error", JOptionPane.ERROR_MESSAGE);
        }
    }

